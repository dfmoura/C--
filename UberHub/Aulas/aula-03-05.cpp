#include <bits/stdc++.h>
using namespace std;

int main(){

    cout << "\nValor com o WHILE"<<endl;
    int i = 0;
    while (i <= 10){
        cout << "O valor do i eh: " << i << endl;
        i++;
    }

    cout << "\nValor com o FOR"<<endl;
    for (int i = 0; i <= 10; i++){
        cout << "O valor do i eh: " << i << endl;
    }
}