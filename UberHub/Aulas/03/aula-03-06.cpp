#include <bits/stdc++.h>
using namespace std;

int main(){

    cout << "\nValor com o WHILE"<<endl;
    int i = 10;
    while (i >= 0){
        cout << "O valor do i eh: " << i << endl;
        i--;
    }

    cout << "\nValor com o FOR"<<endl;
    for (int i = 10; i >= 0; i--){
        cout << "O valor do i eh: " << i << endl;
    }
}